// If-else statement

let score = 85;
if (score >= 90) {
    console.log("Grade A");
} else if (score >= 80) {
    console.log("Grade: B")
} else{
    console.log("Grade: C");
}

// Switch statement

let fruit = "apple";
switch(fruit) {
    case "apple" :
        console.log("It's an appple");
        break;
    case "banana":
        console.log("It's a banana")
        break;
    default:
        console.log("Unknown fruit")
}